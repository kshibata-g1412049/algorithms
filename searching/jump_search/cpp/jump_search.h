#ifndef JUMP_SEARCH_H
#define JUMP_SEARCH_H

#include <vector>

// ジャンプ探索（Jump Search）
//
// ソート済み配列を一定のブロックサイズ（一般的には√n）でジャンプしながら
// 走査し、targetが含まれている可能性のあるブロックを特定する。
// ブロックの末尾要素がtarget以上になった時点でジャンプを止め、そのブロックの
// 内部を先頭から線形探索する。二分探索よりブロックを「戻る」コストが低く、
// 線形探索よりジャンプによって走査範囲を絞れる点が特徴。
//
// 前提条件: 入力配列arrは昇順にソート済みであること（事前条件の検証は行わない）。
//
// 時間複雑度: O(√n)
// 空間複雑度: O(1)
//
// arr: 昇順にソート済みの探索対象の配列
// target: 探したい値
// 戻り値: targetが見つかった場合はそのインデックス（0始まり。複数該当する
//         場合はいずれかのインデックス）、見つからなければ-1
int jumpSearch(const std::vector<int>& arr, int target);

#endif // JUMP_SEARCH_H
