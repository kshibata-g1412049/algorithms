#ifndef RRT_STAR_H
#define RRT_STAR_H

#include <array>
#include <tuple>
#include <utility>
#include <vector>

// RRT*（Rapidly-exploring Random Tree Star / 漸近最適RRT）
//
// 基本のRRTを改良し、漸近的最適性（ノード数を増やすほど経路コストが最適解に
// 収束する性質）を持たせたサンプリングベースの経路計画アルゴリズム。
// 2次元連続空間上で、startを根とする木をランダムサンプリングによって
// 段階的に広げていき、goal付近まで到達する経路を探索する。各反復で以下を行う:
//   1. 探索空間内からランダムに点（サンプル点）を1つ選ぶ（goalバイアスなし、
//      基本RRTと同じ）。
//   2. 木の中でサンプル点に最も近いノード（最近傍ノード）を見つける。
//   3. 最近傍ノードからサンプル点方向にstepSize分だけ進んだ新しい点を計算する
//      （これを「ステア」と呼ぶ）。
//   4. 最近傍ノードから新しい点への線分が障害物と衝突していなければ次に進む。
//   5. 親選択（choose parent、RRT*独自）: 新しい点から一定の近傍半径以内にある
//      既存ノードを全て探し（近傍集合）、その中から
//      「ノードのコスト（startからの累積距離）+ そのノードから新しい点までの距離」
//      が最小になり、かつ間の線分が衝突しないノードを新しい点の親として選ぶ。
//      これにより、単純な最近傍ノードではなく、より低コストな経路を与える
//      ノードが親になることがある。近傍集合が空、または全て衝突する場合は
//      通常通り最近傍ノードを親にする。
//   6. 新しい点をそのコストとともに木に追加する。
//   7. リワイヤ（rewire、RRT*独自）: 新しいノードの近傍集合（手順5と同じ近傍半径）
//      にある既存の各ノードについて、「新しいノードのコスト + 新しいノードから
//      その既存ノードまでの距離」が現在のコストより小さく、かつ間の線分が
//      衝突しないなら、その既存ノードの親を新しいノードに付け替えてコストを
//      更新する。この付け替えによってコストが変化した既存ノードの子孫すべての
//      コストも再帰的に更新する。
//   8. 新しいノードがgoalからgoalRadius以内であれば、その時点で経路を復元して
//      返す。RRT*は本来、漸近的最適性のために全イテレーションを回して最良コスト
//      の経路を保持する設計が望ましいが、本実装では学習用途のシンプルさを優先し、
//      goalに最初に到達した時点で経路を即座に返す（基本RRTと同じ「最初に見つ
//      かった経路を返す」方式）。より高度な実装では、全イテレーション終了後に
//      goal近傍の全ノードからコスト最小のものを選んで経路を復元する。
//
// 時間・空間複雑度:
//   最近傍探索・近傍探索ともに、学習用途のため木の全ノードを線形走査する
//   ナイーブな実装（いずれもO(n)、nは現在の木のノード数）になっている。
//   本格的な実装ではKD-tree等の空間分割構造を使って高速化する。
//   1イテレーションあたり、最近傍探索にO(n)、親選択の近傍探索にO(n)、
//   リワイヤでも近傍ノード分（最大O(n)）の追加処理が必要になるため、
//   1イテレーションあたりO(n)程度。合計でO(n^2)程度（n = maxIterations、
//   最悪ケース）。空間複雑度はノードと親子関係を保持するためO(n)。
//
// obstacles: 障害物のリスト。各要素は(center_x, center_y, radius)のタプル。
// bounds: 探索空間の矩形領域 {xmin, ymin, xmax, ymax}。
// start: 探索開始点(x, y)。
// goal: 目標点(x, y)。
// goalRadius: goalからこの半径以内に到達したら成功とみなす。
// stepSize: 1ステップで木を伸ばす距離。
// maxIterations: 最大反復回数。これを超えても到達できなければ空の経路を返す。
// seed: 乱数生成器（std::mt19937）を初期化するシード値（再現性確保のため必須）。
//
// 戻り値: startからgoal付近までの経路（(x,y)の点のリスト）。
//         先頭がstart、末尾がgoal付近の到達点。見つからなければ空のvector。
std::vector<std::pair<double, double>> rrtStar(
    const std::vector<std::tuple<double, double, double>>& obstacles,
    std::array<double, 4> bounds, std::pair<double, double> start,
    std::pair<double, double> goal, double goalRadius, double stepSize,
    int maxIterations, unsigned int seed);

#endif  // RRT_STAR_H
