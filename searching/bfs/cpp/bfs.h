#ifndef BFS_H
#define BFS_H

#include <vector>

// 幅優先探索（Breadth-First Search, BFS）
//
// グラフを開始ノードから「近いノードから順に」探索するアルゴリズム。
// キュー（FIFO）を使い、開始ノードをキューに入れた後、キューの先頭から
// ノードを取り出して隣接ノードを調べ、未訪問の隣接ノードを隣接リストの
// 並び順でキューに追加していく。これを繰り返すことで、開始ノードからの
// 距離が近い順にノードを訪問する。
//
// 訪問済みノードを管理する配列を使うことで、サイクルがあっても
// 同じノードを2回訪問せず、無限ループにならない。
//
// グラフは0始まりの隣接リスト（adjacency[i]がノードiの隣接ノード番号の
// リスト）で表現する。
//
// 時間複雑度: O(V + E)（Vはノード数、Eは辺数）
// 空間複雑度: O(V)（訪問済み配列とキューのため）
//
// adjacency: 0始まりの隣接リスト
// start: 探索を開始するノード番号（0始まり）
// 戻り値: startから到達可能なノードの訪問順序（startから到達できない
//         ノードは含まれない）
std::vector<int> bfs(const std::vector<std::vector<int>>& adjacency, int start);

#endif // BFS_H
