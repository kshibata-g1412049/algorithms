% test_dfs.m
%
% dfs関数のテストスクリプト（MATLAB/Octave両対応）。
% このフォルダを単独で取り出してそのまま実行できる（外部依存なし）。
%
% MATLABは1始まりインデックスのため、ノード番号もすべて1始まりとして扱う。
%
% 実行方法: MATLAB/Octave上でこのスクリプトを実行する。
%   >> test_dfs
% 全テストが成功すれば "All tests passed." が表示される。

% 単一ノードのグラフ（隣接リストが空のノード1つ）
adjacency = {[]};
assert(isequal(dfs(adjacency, 1), [1]));

% 線形グラフ（1-2-3-4が一直線につながっている）
adjacency = {[2], [1, 3], [2, 4], [3]};
assert(isequal(dfs(adjacency, 1), [1, 2, 3, 4]));

% 分岐のある木構造のグラフ
%   1 -- 2 -- 4
%   |    |
%   3    5
%
% 1から2へ進み、2の未訪問隣接ノードのうち最初の4へ進む。4はそれ以上進めず
% 2に戻り、次の未訪問隣接ノード5へ進む。5からも進めず2, 1に戻り、最後に3へ進む。
adjacency = {[2, 3], [1, 4, 5], [1], [2], [2]};
assert(isequal(dfs(adjacency, 1), [1, 2, 4, 5, 3]));

% サイクルを含むグラフ（1-2-3-4-1のループ）。無限ループしないことを確認する。
adjacency = {[2], [3], [1, 4], [3]};
assert(isequal(dfs(adjacency, 1), [1, 2, 3, 4]));

% 孤立ノードを含む非連結グラフ。startから到達できないノードは結果に含まれない。
adjacency = {[2], [1], []};
assert(isequal(dfs(adjacency, 1), [1, 2]));

% BFSとDFSで訪問順序が異なることを確認するための、複数の子を持つグラフ
%   1 -- 2 -- 5
%   |
%   3
%   |
%   4
%
% 1から最初の未訪問隣接ノード2へ進み、2の未訪問隣接ノード5へ進む。
% 5から進めず2, 1に戻り、次の未訪問隣接ノード3へ進み、戻って4へ進む。
adjacency = {[2, 3, 4], [1, 5], [1], [1], [2]};
assert(isequal(dfs(adjacency, 1), [1, 2, 5, 3, 4]));

disp('All tests passed.');
