#ifndef BUBBLE_SORT_H
#define BUBBLE_SORT_H

#include <vector>

// バブルソート（Bubble Sort）
//
// 隣り合う要素を比較し、順序が逆であれば交換するという操作を
// 配列の先頭から末尾まで繰り返すことで、1回のパスごとに
// 未確定の最大値を末尾側に「泡（バブル）」のように浮かび上がらせていくアルゴリズム。
// パスを繰り返すたびに、ソート済みの要素が末尾から1つずつ確定していく。
//
// 時間複雑度: 最良 O(n)（既にソート済みの場合は早期終了する）
//             平均/最悪 O(n^2)
// 空間複雑度: O(1)（インプレースソート）
//
// arr: ソート対象の配列（破壊的に変更される）
void bubbleSort(std::vector<int>& arr);

#endif // BUBBLE_SORT_H
